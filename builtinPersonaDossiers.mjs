function createConcern(specId, [title, object, type, description, intensity, valence, arousal, triggers], index) {
  return {
    id: `concern_${specId}_${index + 1}`,
    title,
    object,
    type,
    description,
    intensity,
    valence,
    arousal,
    triggers,
    possibleResolutions: ["被认真听见", "处境有具体变化", "关系压力降低"],
    createdAt: timestamp,
    decayRate: 0.015,
    status: "active",
  };
}

